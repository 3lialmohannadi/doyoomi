export function t(key: string, lang: Language): string {
  return translations[lang][key] ?? translations['en'][key] ?? key;
}

export function resolveDisplayName(
  nameAr: string | undefined | null,
  nameEn: string | undefined | null,
  lang: Language,
  fallback?: string,
): string {
  if (lang === 'ar') return nameAr?.trim() || nameEn?.trim() || fallback || '';
  return nameEn?.trim() || nameAr?.trim() || fallback || '';
}

export function getGreeting(lang: Language): string {
  const hour = new Date().getHours();
  if (hour >= 5 && hour < 12) return t('goodMorning', lang);
  if (hour >= 12 && hour < 18) return t('goodAfternoon', lang);
  return t('goodEvening', lang);
}

export function getGreetingEmoji(): string {
  const hour = new Date().getHours();
  if (hour >= 5 && hour < 12) return '🌅';
  if (hour >= 12 && hour < 18) return '☀️';
  return '🌙';
}

export function getPersonalizedGreeting(name: string | undefined | null, lang: Language): string {
  const greeting = getGreeting(lang);
  const emoji = getGreetingEmoji();
  const trimmedName = (name ?? '').trim();
  if (trimmedName) {
    const comma = lang === 'ar' ? '،' : ',';
    return `${greeting}${comma} ${trimmedName} ${emoji}`;
  }
  return `${greeting} ${emoji}`;
}
